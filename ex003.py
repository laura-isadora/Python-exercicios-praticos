# Dar as instruções iniciais
print('Olá! para saber a média do aluno, digite as notas abaixo:')
n1 = float(input('Digite a primeira nota: '))
n2 = float(input('Digite a segunda nota: '))

# Calcular a média e imprimir na tela
media = float(n1 + n2) /2
print('A média do aluno é: {:.2f}'.format(media))
